    /**
     * converts the parameter value into the desired unit
     * the result of the conversion is sent to the label: outputResult
     * @param value
     * value is the number the user enters in the text field: inputValue
     * parameter: value is also universal to all 4 conversion methods
     * @return 
     */
    private double inchesToCentimetres(double value) {
       double centimetres;
       centimetres = value * 2.54;
       outputResult.setText(value + " inches" + " = " + centimetres + " centimetres");
       return value;
      }
    
    
    private double feetToCentimetres (double value) {
        double centimetres;
        centimetres = value * 30;
        outputResult.setText(value + " feet " + "= " + centimetres + " centimetres" );
        return value; 
    }
    private double yardsToMetres (double value) {
      double metres;
      metres = value * 0.91;
      outputResult.setText(value + " yards " + "= " + metres + " metres");
      return value;
    }
    private double milesToKilometres (double value) {
        double kilometres;
        kilometres = value * 1.6;
        outputResult.setText(value + " miles " + "= " + kilometres + " kilometres");
        return value; 
    }
    
