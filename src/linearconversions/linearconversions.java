    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice = Integer.parseInt(inputChoice.getText());
        
        double value = Double.parseDouble(inputValue.getText());
        switch (choice)
        {
            case 1: 
            {
                inchesToCentimetres(value);
                break;
            }
            case 2:
            {
                feetToCentimetres(value);
                break;
            }
            case 3:
            {
                yardsToMetres(value);
                break;
            }
            case 4:
            {
                milesToKilometres(value);
                break;
            }
            default:
            {
                outputResult.setText("Error option " + choice + " is not available");
            }
        }
        // this decides which method to use depending on the choice that is entered
    }//GEN-LAST:event_jButton1ActionPerformed

